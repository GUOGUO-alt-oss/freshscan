/**
 * FreshScan M3 Theme — 日系精致风格 (Japanese Minimalist).
 *
 * Wraps [MaterialTheme] with:
 * - Full 29-role M3 color scheme (Light + Dark)
 * - Noto Serif/Sans SC typography scale (15 styles)
 * - Unified 3-tier shape system
 * - Semantic color tokens via [LocalSemanticColors]
 *
 * Ref: docs/uiv2.md
 */
@Composable
fun FreshScanTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme
    val semanticColors = if (darkTheme) DarkSemanticColors else LightSemanticColors

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    CompositionLocalProvider(
        LocalSemanticColors provides semanticColors
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = AppTypography,
            shapes = AppShapes,
            content = content
        )
    }
}
